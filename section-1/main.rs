// P1: hello world in rust.

// main function. first to run. (no semicolon at the end, is not a expression statement).
fn main() {
    // NOTE: Rust it's ahead-of-time compiled, can run compiled executable without needs installing rust. 
    // when wrote "!", we are using rust-macro, allows using "n" variable arguments.
    // semicolon cause, it's an expression statement.
    println!("hello world"); // indent must have 4 spaces.
}
